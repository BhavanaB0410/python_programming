'''Class: A class is a user-defined blueprint or prototype for creating objects. 
It encapsulates data for the object and methods to manipulate that data.'''

'''Object: An object is an instance of a class. 
It represents a specific entity with attributes and behaviors defined by its class.'''


# Defining a class
class Dog:
    def bark(self):
        print("Woof!")

# Creating an object of the Dog class
my_dog = Dog()
my_dog.bark()  # Output: Woof!
