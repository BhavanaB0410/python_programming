text = "Hello, World!"

# Slicing the string
substring1 = text[0:5]   # 'Hello'
substring2 = text[7:]    # 'World!'
substring3 = text[:5]    # 'Hello'
substring4 = text[-6:]    # 'World!'

print(f"Substring 1: {substring1}")
print(f"Substring 2: {substring2}")
print(f"Substring 3: {substring3}")
print(f"Substring 4: {substring4}")
