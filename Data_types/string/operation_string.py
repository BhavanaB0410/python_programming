text1 = "Hello"
text2 = "World!"

# Concatenation
concatenated = text1 + " " + text2  # 'Hello World!'

# Repetition
repeated = text1 * 3  # 'HelloHelloHello'

# Length of the string
length = len(concatenated)  # 13

print(f"Concatenated: {concatenated}")
print(f"Repeated: {repeated}")
print(f"Length of concatenated string: {length}")
