# Assignment Operators Example

x = 10  # Basic assignment
print("Initial value of x:", x)

# 1. Addition Assignment (+=)
x += 5  # Equivalent to x = x + 5
print("After x += 5:", x)  # x is now 15

# 2. Subtraction Assignment (-=)
x -= 3  # Equivalent to x = x - 3
print("After x -= 3:", x)  # x is now 12

# 3. Multiplication Assignment (*=)
x *= 2  # Equivalent to x = x * 2
print("After x *= 2:", x)  # x is now 24

# 4. Division Assignment (/=)
x /= 4  # Equivalent to x = x / 4
print("After x /= 4:", x)  # x is now 6.0 (float result)

# 5. Modulus Assignment (%=)
x %= 5  # Equivalent to x = x % 5
print("After x %= 5:", x)  # x is now 1.0

# 6. Exponentiation Assignment (**=)
x **= 3  # Equivalent to x = x ** 3
print("After x **= 3:", x)  # x is now 1.0

# 7. Floor Division Assignment (//=)
x = 10
x //= 3  # Equivalent to x = x // 3
print("After x //= 3:", x)  # x is now 3 (integer result)
