# Example of variable assignment and usage

# 1. Integer variable
age = 25
print("Age:", age)

# 2. Float variable
height = 5.9
print("Height:", height)

# 3. String variable
name = "Alice"
print("Name:", name)

# 4. Boolean variable
is_student = True
print("Is Student:", is_student)

# 5. List variable (a collection of items)
hobbies = ["reading", "traveling", "swimming"]
print("Hobbies:", hobbies)

# 6. Dictionary variable (key-value pairs)
person = {
    "name": "Alice",
    "age": 25,
    "height": 5.9
}
print("Person Dictionary:", person)

# 7. Modifying variables
age += 1  # Increment age by 1
print("New Age:", age)

# 8. Using variables in expressions
total_height = height * 2  # Calculating total height for two people
print("Total Height for Two:", total_height)

# 9. String concatenation
greeting = "Hello, " + name + "!"
print(greeting)
