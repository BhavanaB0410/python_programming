# Arithmetic Operators Example

x = 10
y = 5
result = x + y
print("Addition (10 + 5):", result)

result = x - y
print("Subtraction (10 - 5):", result)

result = x * y
print("Multiplication (10 * 5):", result)

result = x / y  # Returns a float
print("Division (10 / 5):", result)

result = x // y  # Returns an integer
print("Floor Division (10 // 5):", result)

result = x % y  # Returns the remainder
print("Modulus (10 % 5):", result)

# Exponentiation (**)
result = x ** y  # 10 raised to the power of 5
print("Exponentiation (10 ** 5):", result)
